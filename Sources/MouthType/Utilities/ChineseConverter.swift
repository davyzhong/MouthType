    /// 将繁体中文转换为简体中文
    /// - Parameter text: 输入的繁体中文文本
    /// - Returns: 转换后的简体中文文本
    func toSimplified(_ text: String) -> String {
        var result = text
        for (traditional, simplified) in traditionalToSimplifiedMap {
            result = result.replacingOccurrences(of: traditional, with: simplified)
        }
        return result
    }

    /// 检查文本是否包含繁体中文字符
    /// - Parameter text: 输入文本
    /// - Returns: 如果包含繁体中文则返回 true
    func containsTraditional(_ text: String) -> Bool {
        for (traditional, _) in traditionalToSimplifiedMap {
            if text.contains(traditional) {
                return true
            }
        }
        return false
    }
